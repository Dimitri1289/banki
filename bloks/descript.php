     <summary>&nbsp;&nbsp;&nbsp;Кредитные карты</summary>
       <p>
Кредитные карты постепенно вытесняют с рынка банковских услуг потребительские ссуды. Банковская карточка – это возможность оперативного получения денежной суммы. Пополняемая денежная линия, разнообразные тарифы и дополнительные услуги смело можно отнести к преимуществам их использования</p>
     </details>
      <details>
     <summary>&nbsp;&nbsp;&nbsp;Как оформить кредитную карту? </summary>
      <p>Получение пластика предполагает несколько этапов. 
      <br>Первый – обращение в филиал банка.
      <br>Второй - заполнение заявление в режиме реального времени на финансовом портале, либо на сайте финансовой компании. Помимо подачи запроса, заёмщик предоставляет список необходимых документов. Он отличается в зависимости от организации, размера займа, срока действия, процентной ставки, наличия истории платежей.
При наличии положительной информации о предыдущих погашениях и, если это не является нарушением политики банковской организации, достаточно предоставить документ, удостоверяющий личность. Проверка данных занимает от нескольких часов до пары рабочих дней.<br>
После получения одобрения со стороны банка остаётся забрать пластик в отделении банковской организации, либо заказать услугу доставки через курьера. Средний срок изготовления именной карты составляет 3 рабочих дня.
В финансовых организациях действует услуга моментального получения карточки. Речь идёт о неименном пластике, процентная ставка будет выше, а кредитный лимит ниже, нежели при оформлении именного аналога.
</p>
     </details>
      <details>
     <summary>&nbsp;&nbsp;&nbsp;Требования к заёмщику</summary>
      <p>Оформить пластик в банке с хорошим лимитом и сроком может клиент, соответствующий следующим требованиям:<br>
• Возрастной ценз устанавливается в каждой финансовой организации. В среднем он составляет от 21 года до 60 лет.<br>
• Гражданство, подтверждаемое паспортом гражданина РФ.<br>
• Положительная кредитная история, начинающаяся после первого же обращения в банк или МФО с целью получения займа. Информация о погашениях отражает все случаи нарушения договора.<br>
• Справка о доходах. Заявитель предоставляет в банк документы, подтверждающие платёжеспособность, в частности форму 2-НДФЛ.
</p>
