     <summary>&nbsp;&nbsp;&nbsp;Условия предоставления кредитной карты </summary>
      <p>Условия кредитования
Условия зависят как от политика банка, так и от самого заявителя. Кредитный лимит представляет собой максимальный размер денежных средств, перечисляемые заёмщику, которая может меняться в процессе использования карточки. Сумма регламентирована банковыми тарифами, находит отражение в кредитном договоре. Денежный лимит полностью восстанавливается после погашения сумму. Таким образом, деньгами можно пользоваться до окончания срока действия карты.
Процентная ставка – это сумма, которая будет взиматься с владельца по истечению льготного периода. Размер ставки зависит от категории самого клиента и политики банка. Повторным клиентам с положительной историей предлагают процентную ставку ниже, чем человеку, в первый раз обратившемуся в финансовую организацию.
</p>
